
CREATE TABLE MEMBER(
	MEMBERNUM INT PRIMARY KEY,
	ID VARCHAR(100) UNIQUE NOT NULL,
	PW VARCHAR(100) NOT NULL,
	NAME VARCHAR(100) NOT NULL,
	NICKNAME VARCHAR(100) UNIQUE NOT NULL,
	BIRTH DATE DEFAULT SYSDATE,
	PH VARCHAR(100) UNIQUE,
	PROFILE VARCHAR(500),
	GRADE VARCHAR(100)
)









